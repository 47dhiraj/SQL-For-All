    Syntax of Select Statement : --> SELECT column_name FROM table;


    Some Basic Examples of Select Statement:


    For Selecting single column from a table
    > SELECT prod_name FROM Products;



    For Selecting multiple columns from a table
    > SELECT prod_name, prod_id, prod_price FROM Products;



    For Selecting all the columns from a table using Wildcard character (*)
    > SELECT * FROM Products;


